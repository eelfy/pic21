int		ft_is_prime(int nb)
{
	int i;
	int prm;

	prm = 0;
	i = 1;
	if (nb <= 1)
	{
		return (0);
	}
	if (nb % 2 == 0 || nb % 5 == 0)
		return (0);
	while (i <= nb)
	{
		if (nb % i == 0)
			prm++;
		i++;
	}
	if (prm == 2)
		return (1);
	else
		return (0);
}
