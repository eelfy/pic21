char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int i;

	i = 0;
	while (dest[i] != '\0' && n != 0)
	{
		dest[i] = src[i];
		++i;
		--n;
	}
	dest[i] = '\0';
	return (dest);
}
