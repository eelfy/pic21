int		ft_str_is_numeric(char *str)
{
	int i;
	int answ;

	i = 0;
	if (str[0] == '\0')
	{
		answ = 1;
	}
	while (str[i] != '\0')
	{
		if (str[i] >= 48 && str[i] <= 57)
		{
			answ = 1;
		}
		else
		{
			answ = 0;
			break ;
		}
		++i;
	}
	return (answ);
}
