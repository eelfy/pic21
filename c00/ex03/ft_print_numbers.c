#include <unistd.h>

void	ft_print_numbers(void);

void	ft_print_numbers(void)
{
	char zr;

	zr = 48;
	while (zr <= 57)
	{
		write(1, &zr, 1);
		zr++;
	}
	write(1, "\n", 1);
}
