int		ft_strcmp(char *s1, char *s2)
{
	int res;
	int i;

	res = 0;
	i = 0;
	while (s1[i] != '\0' && s1[i] == s2[i] && s2[i] != '\0')
	{
		i++;
	}
	res = s1[i] - s2[i];
	return (res);
}
